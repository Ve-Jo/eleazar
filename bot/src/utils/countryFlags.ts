// Get flag emoji for country code
type CountryMap = Record<string, string>;

type CountryEntry = {
  code: string;
  name: string;
  flag: string;
};

const countryFlagsMap: CountryMap = countryFlags;
const countryNamesMap: CountryMap = countryNames;

// Get flag emoji for country code
export function getCountryFlag(countryCode: string | null | undefined): string | null {
  if (!countryCode) return null;
  return countryFlagsMap[countryCode.toUpperCase()] || null;
}

// Get country name for country code
export function getCountryName(countryCode: string | null | undefined): string | null {
  if (!countryCode) return null;
  return countryNamesMap[countryCode.toUpperCase()] || null;
}

// Validate country code
export function isValidCountryCode(countryCode: string | null | undefined): boolean {
  if (!countryCode) return false;
  return Object.prototype.hasOwnProperty.call(countryFlagsMap, countryCode.toUpperCase());
}

// Get all countries with names and flags for selection
export function getAllCountries(): CountryEntry[] {
  return Object.keys(countryFlags)
    .map((code) => ({
      code,
      name: countryNamesMap[code],
      flag: countryFlagsMap[code],
    }))
    .filter((entry): entry is CountryEntry => !!entry.name && !!entry.flag)
    .sort((a, b) => a.name.localeCompare(b.name));
}
